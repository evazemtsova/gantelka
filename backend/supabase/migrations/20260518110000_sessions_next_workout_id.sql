-- ============================================================================
-- sessions.next_workout_id: связь со следующей запланированной тренировкой
--
-- Раньше сохранялась только дата (next_workout_date). Это позволяет показать
-- юзеру «следующая тренировка X 19 мая», а не просто дату без названия.
-- on delete set null — если шаблон тренировки удалят, ссылка обнулится,
-- сама сессия не пострадает.
-- ============================================================================

alter table public.sessions
  add column next_workout_id uuid references public.workouts(id) on delete set null;
